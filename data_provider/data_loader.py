class Dataset_ETT_hour(Dataset):
    # 1.初始化参数
    def __init__(self, root_path, data_path='ETTh1.csv',
                 flag='train', size=None,features='S',
                 target='OT', scale=True, timeenc=0, freq='h'):
        self.root_path = root_path
        self.data_path = data_path

        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        if size == None:
            self.seq_len = 4 * 4 * 24
            self.label_len = 4 * 24
            self.pred_len = 4 * 24
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
            # size [seq_len, label_len, pred_len]

        self.features = features#特征类型，M多变量，S单变量
        self.target = target #目标变量
        self.scale = scale #标准化
        self.timeenc = timeenc #时间编码方式。=0，直接提取例如月份、日期、星期几和小时这些特征；=1，使用周期性编码（如正弦和余弦）生成时间特征
        self.freq = freq#频率

        self.__read_data__()

    # 2.读取数据、划分数据集、日期数据进行编码
    def __read_data__(self):#方法名以双下划线 __ 开头，只能在类的内部被访问， 是一个私有方法
        self.scaler = StandardScaler() #标准化
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]#数据集起始位置
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]#数据集结束位置
        border1 = border1s[self.set_type] #set_type 决定了当前数据集的类型（训练集、验证集或测试集）
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]#cols_data 是一个包含df_raw从第二列开始的所有列名的列表
            df_data = df_raw[cols_data]#包含 cols_data 中指定的列
        elif self.features == 'S':
            df_data = df_raw[[self.target]]#只选择目标变量列。[[ ]]返回数据框，[ ]返回该列的数据

        if self.scale:#如果self.scale=True，进行标准化
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)#用训练集拟合StandardScaler，计算均值和标准差。
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values#否则使用原始数据

        df_stamp = df_raw[['date']][border1:border2]#border1:border2范围内的date列
        df_stamp['date'] = pd.to_datetime(df_stamp.date)#将 date 列数据转换为日期时间格式

        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)#从data列中，对每一行应用lambda函数，提取“month”值,将其存储为新的列 month
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], axis=1).values#axis=1:沿列的方向进行操作, axis=0:沿行的方向
            #删除date 列。将剩余的列转换为 NumPy 数组形式
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)#对特征矩阵进行转置，原来是（时间点，特征），现在是（特征，时间点）

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    #3.获取数据项
    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len#标签长度
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]#输入序列(seq_len, n_features)
        seq_y = self.data_y[r_begin:r_end]#目标序列(label_len + pred_len, n_features)
        seq_x_mark = self.data_stamp[s_begin:s_end]#输入序列时间戳特征
        seq_y_mark = self.data_stamp[r_begin:r_end]#目标序列时间戳特征

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    #4.返回样本长度
    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    # 5.将标准化之后的数据还原成原始数据
    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


